def get_locale():
    lang = session.get('lang', DEFAULT_LANGUAGE)
    return lang if lang in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def _(key, **kwargs):
    lang = get_locale()
    text = MESSAGES.get(lang, {}).get(key) or MESSAGES.get(DEFAULT_LANGUAGE, {}).get(key) or key
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text


def translate_action(action):
    if not action:
        return ''
    if action in MESSAGES.get(get_locale(), {}):
        return _(action)
    key = LEGACY_AUDIT_ACTIONS.get(action)
    if key:
        return _(key)
    return action


def translate_admin_note(note):
    if not note:
        return note
    if note.startswith('admin_note.'):
        return _(note)
    key = LEGACY_ADMIN_NOTES.get(note)
    return _(key) if key else note


def translate_audit_details(action, details):
    if not details:
        return ''
    key = action if action.startswith('audit.') else LEGACY_AUDIT_ACTIONS.get(action)
    if not key and get_locale() == 'zh':
        return details
  # Legacy Chinese detail patterns
    import re
    m = re.match(r'已用: ([\d.]+)→([\d.]+) GB, 总量: ([\d.]+) GB', details)
    if m:
        return _('audit.update_space_detail', old=m.group(1), used=m.group(2), total=m.group(3))
    m = re.match(r'申请管理硬盘 (.+)', details)
    if m:
        return _('audit.request_manage_detail', disk=m.group(1))
    m = re.match(r'申请退还硬盘 (.+)', details)
    if m:
        return _('audit.request_return_detail', disk=m.group(1))
    m = re.match(r'(.+) 退还硬盘 (.+)', details)
    if m:
        return _('audit.approve_return_detail', user=m.group(1), disk=m.group(2))
    m = re.match(r'将 (.+) 管理权交给 (.+)', details)
    if m:
        return _('audit.approve_request_detail', disk=m.group(1), user=m.group(2))
    m = re.match(r'硬盘 (.+)，申请人 (.+)', details)
    if m:
        return _('audit.reject_request_detail', disk=m.group(1), user=m.group(2))
    m = re.match(r'用户名: (.+)，(保留|清除)其数据', details)
    if m:
        action_word = _('audit.delete_user_keep') if m.group(2) == '保留' else _('audit.delete_user_clear')
        return _('audit.delete_user_detail', username=m.group(1), action=action_word)
    m = re.match(r'管理员重置了用户 (.+) 的密码', details)
    if m:
        return _('audit.reset_password_detail', username=m.group(1))
    m = re.match(r'用户名: (.+)', details)
    if m and key == 'audit.edit_user':
        return _('audit.edit_user_detail', username=m.group(1))
    if details == '用户自行修改密码':
        return _('audit.change_password_detail')
    return details


