def format_date(d, long=False):
    if not d:
        return ''
    if isinstance(d, datetime):
        d = d.date()
    if get_locale() == 'en':
        return d.strftime('%B %d, %Y') if long else d.strftime('%Y-%m-%d')
    return d.strftime('%Y年%m月%d日') if long else d.strftime('%Y-%m-%d')


def format_datetime(dt):
    if not dt:
        return ''
    if get_locale() == 'en':
        return dt.strftime('%Y-%m-%d %H:%M')
    return dt.strftime('%Y-%m-%d %H:%M')
